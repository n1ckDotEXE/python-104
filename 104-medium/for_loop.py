# print numbers from 0 to 99
print("All numbers from 0 to 99:")
for number in range(100):
    print(number)

# print even numbers from 0 to 99
print("Even numbers from 0 to 99:")
for number in range(100):
    if number % 2 == 0:
        print(number)

# print odd numbers from 0 to 99
print("Odd numbers from 0 to 99:")
for number in range(100):
    if number % 2 != 0:
        print(number)